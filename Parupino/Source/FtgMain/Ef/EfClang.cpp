namespace GAME
{
	EfClang::EfClang ()
	{
		AddTexture ( _T ( "Ef_Clang.png" ) );
	}

	EfClang::~EfClang ()
	{
	}

	void EfClang::On ( VEC2 center )
	{
		SetDispBase ( G_BASE_POS () );
		SetPos ( center );
		SetFadeOut ( 15 );
		SetValid ( true );
		GrpEf::On ();
	}
}
